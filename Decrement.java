class decrement{
	public static void main (String [] args){
	int a = 4;

	System.out.println("Pre-Decrement ");
	System.out.println("a\t :" +a);
	System.out.println("--a\t :"+ --a);
	System.out.println("a--\t :"+ a--);
	System.out.println("a\t :"+ a);

	int b = 10;

	System.out.println("Post-Decrement ");
	System.out.println("b\t :" +b);
	System.out.println("b--\t :"+ b--);
	System.out.println("--b\t :"+ -b);
	System.out.println("b\t :"+ b);
	}
}