class increament{
	public static void main (String [] args){
	int a = 8;

	System.out.println("Pre-Increament ");
	System.out.println("a\t :" +a);
	System.out.println("++a\t :"+ ++a);
	System.out.println("a++\t :"+ a++);
	System.out.println("a\t :"+ a);

	int b = 8;

	System.out.println("Post-Increament ");
	System.out.println("b\t :" +b);
	System.out.println("b++\t :"+ b++);
	System.out.println("++b\t :"+ ++b);
	System.out.println("b\t :"+ b);
	}
}